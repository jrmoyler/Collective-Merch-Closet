import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const source = resolve("public/merch-card");
const outputDir = resolve("src/merch-images");
const output = resolve("src/merch-images.js");
const files = (await readdir(source)).filter((file) => file.endsWith(".webp")).sort();
await mkdir(outputDir, { recursive: true });
const modules = await Promise.all(files.map(async (file, index) => {
  const encoded = (await readFile(resolve(source, file))).toString("base64");
  const variable = `image${String(index + 1).padStart(3, "0")}`;
  const moduleName = `${String(index + 1).padStart(3, "0")}.js`;
  await writeFile(resolve(outputDir, moduleName), `export default "data:image/webp;base64,${encoded}";\n`);
  return { variable, moduleName };
}));

const imports = modules.map(({ variable, moduleName }) => `import ${variable} from "./merch-images/${moduleName}";`).join("\n");
const values = modules.map(({ variable }) => variable).join(",");
await writeFile(output, `// Generated by scripts/embed-merch-images.mjs\n${imports}\nexport default [${values}];\n`);
console.log(`Embedded ${modules.length} garment images across ${outputDir}.`);
